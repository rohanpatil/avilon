            <h4 class="title" style="color: #212529">Why Paani Foundation? Why Water?</h4>
            <p>
              Water presents India’s largest problem and largest opportunity.  Water scarcity turns life into a living hell. Water abundance opens up the possibilities for every kind of human and economic activity. A village with water abundance will typically have substantially higher educational levels, superior health and life expectancy, and incomes that are a multiple of those which suffer from water scarcity.
            </p>

            <h4 class="title" style="color: #212529">Is there a Water Crisis?</h4>
            <p>
              India is classified globally as a water-adequate nation. It has neither abundance nor scarcity. It has enough for its needs.  Yet, increasingly, more and more people do not have water to drink, more and more farmers face drought and starvation, and more and more industries shut down or cannot grow because of a shortage of water.
            </p>

            <h4 class="title" style="color: #212529">Why this Crisis?</h4>
            <h3 style="color: #212529">The crisis is largely man-made and has four key causes:</h3>
            <ul>
              <li><i class="ion-android-checkmark-circle"></i> Pollution </li>
              <li><i class="ion-android-checkmark-circle"></i> Over-Exploitation</li>
              <li><i class="ion-android-checkmark-circle"></i> Irrational Water Management</li>
              <li><i class="ion-android-checkmark-circle"></i> Climate Change</li>
            </ul>

            <h4 class="title" style="color: #212529">Is there a Solution?</h4>
            <p>The heart of the solution is to locally harvest rain water by reducing run-off and losses due to evaporation by recharging the ground water table. In other words, the solution is watershed management.</p>

            <p>This solution is inexpensive and has been used with great success but only by a few hundred villages in different parts of India.  The challenge is to take the solution to scale. To ensure that every single village, town and city harvests rain water.</p>

            <p>The solution obviously also requires a rapid stop to water pollution and irrational use of water.</p>

             <h4 class="title" style="color: #212529">What does the Foundation do?</h4>
             <p>To begin with, the Foundation is working on the question of water only in the state of Maharashtra.</p>
             <p>The Government of Maharashtra has launched the ambitious Jalyukt Shivar Abhiyaan to work on watershed management at the village level. Several NGOs and corporates have adopted villages and are constructing local water conservation works for this purpose. While all of this work is extremely important, we are approaching the challenge from a diametrically different direction.</p>
             <p>We believe that it is the people themselves who have the greatest power and capacity to solve the problem of water. If people are convinced and motivated, they will find the land, the money, the know-how, the labour and whatever else is required to harvest and manage their water. This approach offers the real possibility of a mammoth and rapid scale-up of the effort to solve the water crisis.</p>
            
            <h3 style="color: #212529">Concretely, the Foundation is working on three major fronts:</h3>
             <ul>
              <li>
                <i class="ion-android-checkmark-circle"></i> Motivation 
                <p>People must be convinced that localised rainwater harvesting and watershed management is the way to ensure water security. Farmers, industrialists, citizens of every walk of life – adults and children – everyone will have to be part of the movement. Every person must be convinced that he or she has a role to play in this and that this role can be played easily and joyfully. </p>
                <p>The Foundation aims to use every form of mass media and digital media to motivate people by entertaining them, educating them and emotionally drawing them into this movement.</p>
              </li>
              <li>
                <i class="ion-android-checkmark-circle"></i> Training
                <p>The Foundation is working towards training five persons per village in 30,000 drought-prone villages in Maharashtra to work on watershed management. This will entail training nearly 1.5 lakh people totally.</p>
                <p>This training will have two key components. The first is the technical aspects of watershed management and the second is the leadership challenge which, at heart, is the challenge of getting people together and united for this task.</p>
              </li>
              <li><i class="ion-android-checkmark-circle"></i> A Platform for Resources
                <p>The Foundation aims to create a digital platform that will enable people to:</p>
                - Access technical information from peers and from experts.</br>
                - Access funds through crowdsourcing.</br>
                - Access help from the government.</br>
                - Access mentors and volunteers.</br>
              </li>
              <p>In a nutshell, the Foundation’s focus is to motivate, train and empower people to solve their water crisis through their own efforts. </p>
            </ul>
